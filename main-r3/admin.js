function renderCities() {
    const container = document.getElementById("cityContainer");
    container.innerHTML = "";

    Object.entries(adminData).forEach(([city, data]) => {
        const card = document.createElement("div");
        card.className = "city-card";

        card.innerHTML = `
            <img src="${data.image || 'https://via.placeholder.com/400x220'}">
            <h3>${city}</h3>
            <p>${data.barangays.length} Barangays</p>
        `;

        card.onclick = () => openCity(city);
        container.appendChild(card);
    });
}

