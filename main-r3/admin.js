function addCity(name) {
    if (!name) return;
    adminData[name] = { image: "", barangays: [] };
    renderCities();
}

function addBarangay(city, name) {
    if (!city || !name) return;

    adminData[city].barangays.push({
        name,
        image: "",
        admin: { name: "New Barangay Admin", account: "new.admin" },
        officials: [],
        citizens: [],
        posts: []
    });

    renderBarangays(city);
}

