function setActiveNav(section) {
    document.querySelectorAll(".sidebar-link").forEach(btn => {
        btn.classList.toggle("active", btn.dataset.section === section);
    });
}

function renderAllBarangays() {
    const list = document.getElementById("allBarangayList");
    list.innerHTML = "";

    Object.entries(adminData).forEach(([city, data]) => {
        data.barangays.forEach(b => {
            const card = document.createElement("div");
            card.className = "barangay-card";
            card.innerHTML = `
                <img src="${b.image || barangayPlaceholder}">
                <h3>${b.name}</h3>
                <p>${city} · 1 Admin · ${b.officials.length} Officials · ${b.citizens.length} Citizens</p>
            `;
            card.onclick = () => openBarangay(city, b.name);
            list.appendChild(card);
        });
    });

    if (!list.children.length) {
        list.innerHTML = `<div class="empty">No barangays available yet.</div>`;
    }
}

function renderAllPosts() {
    const feed = document.getElementById("postsFeed");
    feed.innerHTML = "";

    Object.entries(adminData).forEach(([city, data]) => {
        data.barangays.forEach(b => {
            b.posts.forEach(post => {
                const card = document.createElement("div");
                card.className = "post-card";
                card.innerHTML = `
                    <img class="post-image" src="${postPlaceholder}" alt="Post image">
                    <div>
                        <p>${post}</p>
                        <small>${city} · ${b.name}</small>
                    </div>
                    <div class="actions">
                        <button class="edit">Edit</button>
                        <button class="delete">Delete</button>
                    </div>
                `;
                feed.appendChild(card);
            });
        });
    });

    if (!feed.children.length) {
        feed.innerHTML = `<div class="empty">No posts available yet.</div>`;
    }
}

function showSection(section) {
    ["dashboardSection", "allBarangaysSection", "postsSection", "settingsSection", "barangayContainer", "barangayDetail"].forEach(id => {
        document.getElementById(id)?.classList.add("hidden");
    });

    if (section === "dashboard" || section === "cities") {
        document.getElementById("dashboardSection").classList.remove("hidden");
        const title = section === "cities" ? "Manage Cities" : "Welcome back, Admin";
        const subtitle = section === "cities" ? "Browse and edit city records." : "Overview of the system activity and quick actions.";
        document.querySelector(".dashboard-head h1").textContent = title;
        document.querySelector(".dashboard-head p").textContent = subtitle;
        renderCities();
        updateStats();
    } else if (section === "barangays") {
        renderAllBarangays();
        document.getElementById("allBarangaysSection").classList.remove("hidden");
    } else if (section === "posts") {
        renderAllPosts();
        document.getElementById("postsSection").classList.remove("hidden");
    } else if (section === "settings") {
        renderSettings();
        document.getElementById("settingsSection").classList.remove("hidden");
    }
}

